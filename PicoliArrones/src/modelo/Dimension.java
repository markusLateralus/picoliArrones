package modelo;

public class Dimension {

	private int ancho,alto;
	
	public Dimension(int ancho, int alto) {
		this.alto=alto;
		this.ancho=ancho;
	}

	public int getAncho() {
		return ancho;
	}

	public void setAncho(int ancho) {
		this.ancho = ancho;
	}

	public int getAlto() {
		return alto;
	}

	public void setAlto(int alto) {
		this.alto = alto;
	}
	
	
	
}
